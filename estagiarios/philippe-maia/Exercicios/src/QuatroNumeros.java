
public class QuatroNumeros {

	public static void main(String[] args) {
		
		String numero1 = "1";
		String numero2 = "2";
		String numero3 = "3";
		String numero4 = "4";
		
		int number1 = Integer.parseInt(numero1);
		int number2 = Integer.parseInt(numero2);
		int number3 = Integer.parseInt(numero3);
		int number4 = Integer.parseInt(numero4);
		
		int soma = 0;
		
		soma = number1 + number2 + number3 + number4;
		
		System.out.println(soma);
	}
}
